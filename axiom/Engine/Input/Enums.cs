    /// <summary>
    ///    Possible buttons that can be found on a mouse.
    /// </summary>
    [Flags]
    public enum MouseButtons {
		/// <summary>
		///    This is for mouse events without a button change
		/// </summary>
		None = 0,
        /// <summary>
        ///    Typically the left button.
        /// </summary>
        Left = 1,
        /// <summary>
        ///    Typically the right button.
        /// </summary>
        Right = 2,
        /// <summary>
        ///    Typically the middle button (pressing the scroll wheel).
        /// </summary>
        Middle = 4
    }

	/// <summary>
	///		Special keys that can alter input behavior when down.
	/// </summary>
	[Flags]
	public enum ModifierKeys {
		None			= 0,
		Shift			= 1,
		Control			= 2,
		Alt				= 4,
		MouseButton0	= 8,
		MouseButton1	= 16,
		MouseButton2	= 32
	}
